    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if(cbact.getSelectedItem().equals("Actualizar")) {
            enableTrue();
            darcolor();
            dispose();
//             ActualizarMP.txtmaquina.grabFocus();
        }

        if(cbact.getSelectedItem().equals("Guardar")) {
            actualizar=true;
                    
            entradasalidaMP es=new  entradasalidaMP();

     es.setIdES(Integer.parseInt(ActualizarMP.txtid.getText()));
     es.setIdvehiculo(ActualizarMP.txtidv.getText());
     
    if(ActualizarMP.datefs.getDate()==null){
            ActualizarMP.txt1.setText("");
        }
        es.setFechaSalida(ActualizarMP.txt1.getText());
        es.setHoraSalida(ActualizarMP.txths.getText());
        if(ActualizarMP.datefe.getDate()==null){
            ActualizarMP.txt2.setText("");
        }
        es.setFechaEntrada(ActualizarMP.txt2.getText());
        es.setHoraEntrada(ActualizarMP.txthe.getText());
        
        es.setOperador(ActualizarMP.txtoperador.getText());
        es.setNparte(ActualizarMP.txtparte.getText());
        if(ActualizarMP.datefp.getDate()==null){
            ActualizarMP.txt3.setText("");
        }
        es.setFechaparte(ActualizarMP.txt3.getText());
        es.setPlaca(ActualizarMP.txtplaca.getText());
        es.setCliente(ActualizarMP.txtcliente.getText());
        es.setObra(ActualizarMP.txtobra.getText());

        String turno = ActualizarMP.cvturno.getSelectedItem().toString();
        es.setTurno(turno);
        
         String tipoc = ActualizarMP.cbtc.getSelectedItem().toString();
        es.setTipocombustible(tipoc);

        es.setConsumo(ActualizarMP.txtconsumo.getText());
        
           String medicion = ActualizarMP.cbmedicion.getSelectedItem().toString();
        es.setMedicion(medicion);

        es.setComienzo(ActualizarMP.txtcomienzo.getText());
        es.setTermino(ActualizarMP.txttermino.getText());
        es.setHorastrabajadas(ActualizarMP.txtht.getText());
        es.setViajes(ActualizarMP.txtviaje.getText());
        es.setObservacion(ActualizarMP.txtobs.getText());
        es.setSupervisor(ActualizarMP.txtsupervisor.getText());
       
        es.setFECHAM(ActualizarMP.txtfmodif1.getText());
        es.setNOMUSUM(ActualizarMP.txtuser2.getText());
        es.setIP(ActualizarMP.txtip1.getText());


                 
            if(actualizar){
                resultado=dao.ModificarESMP(es);
                JOptionPane.showMessageDialog(rootPane, resultado);
                
                    dao.mostrarEntradaSalidasMP("", "", "", "");
        RegistMP.table.setRowSelectionInterval(0, 0);
                enableFalse();
                quitarcolor();
            }
            dispose();

        }

    }//GEN-LAST:event_jButton1ActionPerformed

