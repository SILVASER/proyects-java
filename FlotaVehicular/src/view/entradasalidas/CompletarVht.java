    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if(cbact.getSelectedItem().equals("Actualizar")) {
            enableTrue();
            darcolor();
            dispose();
//             ActualizarVht.txtmaquina.grabFocus();
        }

        if(cbact.getSelectedItem().equals("Guardar")) {
            actualizar=true;
                    
                       if (ActualizarVht.txtidv.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Debe digitar un codigo vehiculo");
           ActualizarVht.txtidv.grabFocus();
            return;
        }      
            entradasalidaVHT es=new  entradasalidaVHT();
        
                es.setIdES(Integer.parseInt(ActualizarVht.txtid.getText()));
                es.setIdvehiculo(ActualizarVht.txtidv.getText());
                
                
                if(ActualizarVht.datefs.getDate()==null){
                    ActualizarVht.txt1.setText("");
                }
                es.setFechaSalida(ActualizarVht.txt1.getText());
        
                es.setHoraSalida(ActualizarVht.txths.getText());
                
                 if(ActualizarVht.datefe.getDate()==null){
                    ActualizarVht.txt2.setText("");
                }
                es.setFechaEntrada(ActualizarVht.txt2.getText());
                 
        
                es.setHoraEntrada(ActualizarVht.txthe.getText());
                
                es.setConductor(ActualizarVht.txtconductor.getText());
                es.setPlacatracto(ActualizarVht.txtplacat.getText());
                es.setPlacacarreta(ActualizarVht.txtplacac.getText());
                es.setObservaciones(ActualizarVht.txtobs.getText());
        
                es.setDestino(ActualizarVht.txtdestino.getText());
                es.setProductoIda(ActualizarVht.txtproductoida.getText());
                es.setProductoRetorno(ActualizarVht.txtproductoretorno.getText());
                es.setGastos(Double.parseDouble(ActualizarVht.txtgastos.getText()));
                es.setPrecioViaje(Double.parseDouble(ActualizarVht.txtpv.getText()));
                es.setKilometraje(Double.parseDouble(ActualizarVht.txtkilometraje.getText()));
                
                es.setFECHAM(ActualizarVht.txtfmodif1.getText());
                es.setNOMUSUM(ActualizarVht.txtuser.getText());
                es.setIP(ActualizarVht.txtip1.getText());

            if(actualizar){
                resultado=dao.ModificarESVHT(es);
                JOptionPane.showMessageDialog(rootPane, resultado);
                
                    dao.mostrarEntradaSalidasVHT("", "", "", "","");
                    RegistVht.table.setRowSelectionInterval(0, 0);
                            enableFalse();
                            quitarcolor();
                        }
            dispose();

        }

    }//GEN-LAST:event_jButton1ActionPerformed

