    private void totales() {
        int num = tabledc.getRowCount();
        double sumVal = 0, sumIngr=0, sumEgr=0;
        for (int i = 0; i < num; i++) {
            //fila y columna
           // sumCant += Utilidades.objectToDouble(tabledetallecaja.getValueAt(i, 3));
            sumVal += Utilidades.objectToDouble(tabledc.getValueAt(i, 6));
            
           String mov= tabledc.getValueAt(i, 4).toString();
           //System.out.println(dd);
           if(mov.equals("Ingreso")){
               sumIngr += Utilidades.objectToDouble(tabledc.getValueAt(i, 6));
           }
            if(mov.equals("Egreso")){
               sumEgr += Utilidades.objectToDouble(tabledc.getValueAt(i, 6));
           }
        }
        txtsaldofinal.setText("" + sumVal);
        txtingresos.setText("" + sumIngr);
        txtegresos.setText("" + sumEgr);
       
        //txtTotalValor.setText("" + sumVal);
    }
    
