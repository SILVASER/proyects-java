    private void btncomplActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncomplActionPerformed


          nuevo=true;
          
          if (!Utilidades.isDecimal(txtmonto.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Debe digitar un valor numerico");
            txtmonto.setText("");
            txtmonto.grabFocus();
            return;
        }
               
         double precio = Double.parseDouble(txtmonto.getText());
         if ((precio <= 0) || (precio<=0.00)) {
            JOptionPane.showMessageDialog(rootPane, "Debe digitar un valor mayor a cero");
            txtmonto.setText("");
            txtmonto.grabFocus();
            return;
        } 
        
        DetCaja dt=new DetCaja();
            
            dt.setIDAPERTURA(txtcodigo.getText());
            dt.setNOMBRE(txtnombre.getText());
            dt.setDETALLE(txtdetalle.getText());
                        
            String valor = cbdocumento.getSelectedItem().toString();
            dt.setIDDOCUMENTO(dao.verificarDocumento(valor));
            
            dt.setSERIE(txtserie.getText());
            dt.setNUMERO(txtnumero.getText());
           
            dt.setFECHAINGRESO(txtdate.getText());
            dt.setTIPO(cbtipoactividad.getSelectedItem().toString());
            dt.setIDCOLABORADOR(txtcodigocolaborador.getText());
            dt.setCOLABORADOR(txtnombrecolaborador.getText());
            dt.setTIPOCAMBIO(Double.parseDouble(txttipocambio.getText()));
            dt.setMONEDA(cbmonedas.getSelectedItem().toString());
            dt.setFORMAPAGO(txtformapago.getText());
            dt.setMONTO(Double.parseDouble(txtmonto.getText()));         
            dt.setFECHAACT(txtfechaact.getText());
            dt.setUSU(txtuser.getText());
            dt.setIP(txtip.getText());
            
        if(nuevo){
                resultado=dao.GuardarDetalleCaja(dt);
                JOptionPane.showMessageDialog(rootPane, resultado);
  
//                Date fecha=new Date();
                String id = txtcodigo.getText();
                String fechaingreso = Utilidades.formatDate(fecha);
//                dao.mostrarCaja(id, fechaingreso,"");
                
                
                dao.mostrarDetalleCaja(id, fechaingreso);
  
//                    String codigov=txtcodigo.getText();
//                    String fechav=lblfecha.getText();
//
//           dao.mostrarDetalleCaja(codigov, fechav);
                           tabledc.setRowSelectionInterval(0, 0);
//
           totales();
           quitarColor();
           enableFalse();
           btncompl.setEnabled(false);
          
           btnnuevo.setEnabled(true);
//           
           ApCaja apc=new ApCaja();
           apc.setIDAPERTURA(txtcodigo.getText());
           apc.setSALDO(Double.parseDouble(txtsaldofinal.getText()));
           apc.setSALDOFINAL(Double.parseDouble(txtsaldofinal.getText()));
           apc.setEGRESOS(Double.parseDouble(txtegresos.getText()));
           apc.setINGRESOS(Double.parseDouble(txtingresos.getText()));
            
           dao.ModificarApCaja(apc);
        }


    }//GEN-LAST:event_btncomplActionPerformed

