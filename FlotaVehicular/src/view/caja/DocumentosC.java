    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
         if(estado1){
                      Documentos doc=new Documentos();  
 
            if (txtdc.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Debe digitar la descripci√≥n");
            txtdc.grabFocus();
            return;
        }
            
              if (txtdl.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Debe digitar la descripci√≥n");
            txtdl.grabFocus();
            return;
        } 
              
        doc.setDescripcioncorta(txtdc.getText());
        doc.setDescripcionlarga(txtdl.getText());
			

					nuevo = true;
					if (nuevo) {
						String resultado = dao.GuardarDocumento(doc);
						JOptionPane.showMessageDialog(rootPane, resultado);
                                                
                                                dao.mostrarDocumentos();
                                                tabladocumento.setRowSelectionInterval(0, 0);
                                                
                                                   txtcodigo.setText("");
                                                    txtdc.setText("");
                                                    txtdl.setText("");

                                                        txtdc.setBackground(new Color(255,255,255));
                                                       txtdl.setBackground(new Color(255,255,255));
                                                       
                                                      txtdc.setEditable(false);
                                                    txtdl.setEditable(false);
                                                    
                                                     btncancelar.setEnabled(false);
          btnguardar.setEnabled(false);
           btnnuevo.setEnabled(true);
            btnactualizar.setEnabled(false);
                                                    
                                                        dao.listarDocumentos(NuevoMovimiento.cbdocumento);

					}
                }
                
                 if(estado2){
                 Documentos doc=new Documentos();  
 
            if (txtdc.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Debe digitar la descripci√≥n");
            txtdc.grabFocus();
            return;
        }
            
              if (txtdl.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Debe digitar la descripci√≥n");
            txtdl.grabFocus();
            return;
        } 
              
        doc.setIddocumentos(Integer.parseInt(txtcodigo.getText()));
        doc.setDescripcioncorta(txtdc.getText());
        doc.setDescripcionlarga(txtdl.getText());
			

					nuevo = true;
					if (nuevo) {
						String resultado = dao.ActualizarDocumento(doc);
						JOptionPane.showMessageDialog(rootPane, resultado);
                                                
                                                dao.mostrarDocumentos();
                                                tabladocumento.setRowSelectionInterval(0, 0);
                                                
                                                   

                                                        txtdc.setBackground(new Color(255,255,255));
                                                       txtdl.setBackground(new Color(255,255,255));
                                                       
                                                      txtdc.setEditable(false);
                                                    txtdl.setEditable(false);
                                                     btncancelar.setEnabled(false);
          btnguardar.setEnabled(false);
           btnnuevo.setEnabled(true);
            btnactualizar.setEnabled(false);
                                                        dao.listarDocumentos(NuevoMovimiento.cbdocumento);

					}

                }
                
    }//GEN-LAST:event_btnguardarActionPerformed

