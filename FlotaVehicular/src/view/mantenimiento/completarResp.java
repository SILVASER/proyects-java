    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        if(cbact1.getSelectedItem().equals("Actualizar")) {
            enableTrue();
            darcolor();
            dispose();
        }
//
        if(cbact1.getSelectedItem().equals("Guardar")) {
            actualizar=true;
             
                if (ActualizarResp.txtdescripcion.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Debe ingresar la descripción del producto");
            ActualizarResp.txtdescripcion.setText("");
            ActualizarResp.txtdescripcion.grabFocus();
            return;
        } 
                  if (ActualizarResp.txtcant.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Debe completar el campo cantidad");
            ActualizarResp.txtcant.setText("");
            ActualizarResp.txtcant.grabFocus();
            return;
        } 
               // int cant =Integer.parseInt(txtcant.getText());
                   if (!Utilidades.isNumeric(ActualizarResp.txtcant.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Debe digitar un valor numerico");
            ActualizarResp.txtcant.setText("");
            ActualizarResp.txtcant.grabFocus();
            return;
        } 
                   int cant =Integer.parseInt(ActualizarResp.txtcant.getText());
                     if (cant<=0) {
            JOptionPane.showMessageDialog(rootPane, "Debe digitar un valor mayor a 0");
            ActualizarResp.txtcant.setText("");
            ActualizarResp.txtcant.grabFocus();
            return;
        } 
                   
                   
                          if (!Utilidades.isDecimal(ActualizarResp.txtprec.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Debe digitar un valor numerico");
            ActualizarResp.txtprec.setText("");
            ActualizarResp.txtprec.grabFocus();
            return;
        } 
                          
                                          if (!Utilidades.isDecimal(ActualizarResp.txtsubtotal.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Debe digitar un valor numerico en el campo subtotal");
            ActualizarResp.txtsubtotal.setText("");
            ActualizarResp.txtsubtotal.grabFocus();
            return;
        }
                                          if (!Utilidades.isDecimal(ActualizarResp.txtimporteiva.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Debe digitar un valor numerico en el campo importe iva");
            ActualizarResp.txtimporteiva.setText("");
            ActualizarResp.txtimporteiva.grabFocus();
            return;
        }
                                          if (!Utilidades.isDecimal(ActualizarResp.txttotal.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Debe digitar un valor numerico en el campo total");
            ActualizarResp.txttotal.setText("");
            ActualizarResp.txttotal.grabFocus();
            return;
        }
            Repuesto r=new  Repuesto();

            

            r.setIdr(Integer.parseInt(ActualizarResp.txtidr.getText()));
            
            r.setIdmantenimiento(ActualizarResp.txtcodigo.getText());
                r.setDescripcion(ActualizarResp.txtdescripcion.getText());
                r.setMarca(ActualizarResp.txtmarca.getText());
                r.setIva(Double.parseDouble(ActualizarResp.txtiva.getText()));
                r.setImporteiva(Double.parseDouble(ActualizarResp.txtimporteiva.getText()));
                r.setCantidad(Integer.parseInt(ActualizarResp.txtcant.getText()));
                r.setPrecio(Double.parseDouble(ActualizarResp.txtprec.getText()));
                r.setSubtotal(Double.parseDouble(ActualizarResp.txtsubtotal.getText()));
                r.setTotal(Double.parseDouble(ActualizarResp.txttotal.getText()));
                r.setFecham(ActualizarResp.datefa.getText());
                r.setNumusum(ActualizarResp.txtuser.getText());
                r.setIp(ActualizarResp.txtip.getText());
                
          

           
            if(actualizar){
                resultado=dao.ModificarRepuesto(r);
                JOptionPane.showMessageDialog(rootPane, resultado);
                enablefalse();
            int verfilay = tablemantenimiento.getSelectedRow(); 
            String idy = (String) tablemantenimiento.getValueAt(verfilay, 0);
            dao.mostrarRespuesto(idy, "");
            tablerepuestos.setRowSelectionInterval(0, 0);
            quitarcolor();
            }
            dispose();

        }
    }//GEN-LAST:event_jButton2ActionPerformed

