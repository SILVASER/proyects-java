    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        if(cbact1.getSelectedItem().equals("Actualizar")) {
            enableTrue();
            darcolor();
            dispose();
        }

        if(cbact1.getSelectedItem().equals("Guardar")) {
            actualizar=true;
 if (ActualizarMant.cbtm.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debe seleccionar tipo de mantenimiento");
            ActualizarMant.cbtm.grabFocus();
            return;
        }
         
         
          
           if (ActualizarMant.txtnombre.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Debe digitar nombre de actividad");
           ActualizarMant.txtnombre.grabFocus();
            return;
        }
            if (ActualizarMant.txtnomv.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Debe digitar el nombre del vehiculo");
           ActualizarMant.txtnomv.grabFocus();
            return;
        }
                          
              if (!Utilidades.isNumeric(ActualizarMant.txtdiasv.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Debe digitar un valor numerico");
            ActualizarMant.txtdiasv.setText("");
            ActualizarMant.txtdiasv.grabFocus();
            return;
        } 
             
             int cant=Integer.parseInt(ActualizarMant.txtdiasv.getText());
              if (cant<0) {
                JOptionPane.showMessageDialog(rootPane, "Debe digitar un valor mayor o igual a '0' ");
                ActualizarMant.txtdiasv.setText("");
                ActualizarMant.txtdiasv.grabFocus();
                return;
                } 
              
                  if (!Utilidades.isDecimal(ActualizarMant.txtimporte.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Debe digitar un valor numerico");
            ActualizarMant.txtimporte.setText("");
            ActualizarMant.txtimporte.grabFocus();
            return;
        } 
                double importe=Double.parseDouble(ActualizarMant.txtimporte.getText());
              if ((importe<0) || (importe<0.00) ) {
                JOptionPane.showMessageDialog(rootPane, "Debe digitar un valor mayor o igual a '0' ");
                ActualizarMant.txtimporte.setText("");
                ActualizarMant.txtimporte.grabFocus();
                return;
                }  
                  
                      if (!Utilidades.isNumeric(ActualizarMant.txturk.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Debe digitar un valor numerico");
            ActualizarMant.txturk.setText("");
            ActualizarMant.txturk.grabFocus();
            return;
        } 
                      double urk=Double.parseDouble(ActualizarMant.txturk.getText());
              if ((urk<0) || (urk<0.00) ) {
                JOptionPane.showMessageDialog(rootPane, "Debe digitar un valor mayor o igual a '0' ");
                ActualizarMant.txturk.setText("");
                ActualizarMant.txturk.grabFocus();
                return;
                }
                          if (!Utilidades.isNumeric(ActualizarMant.txtvk.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Debe digitar un valor numerico");
            ActualizarMant.txtvk.setText("");
            ActualizarMant.txtvk.grabFocus();
            return;
        } 
                          
                      double vk=Double.parseDouble(ActualizarMant.txtvk.getText());
              if ((vk<0) || (vk<0.00) ) {
                JOptionPane.showMessageDialog(rootPane, "Debe digitar un valor mayor o igual a '0' ");
                ActualizarMant.txtvk.setText("");
                ActualizarMant.txtvk.grabFocus();
                return;
                }
                              if (!Utilidades.isNumeric(ActualizarMant.txtsrk.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Debe digitar un valor numerico");
            ActualizarMant.txtsrk.setText("");
            ActualizarMant.txtsrk.grabFocus();
            return;
        } 
                                double srk=Double.parseDouble(ActualizarMant.txtsrk.getText());
              if ((srk<0) || (srk<0.00) ) {
                JOptionPane.showMessageDialog(rootPane, "Debe digitar un valor mayor o igual a '0' ");
                ActualizarMant.txtsrk.setText("");
                ActualizarMant.txtsrk.grabFocus();
                return;
                }
                             
           if (ActualizarMant.datefechamant.getDate()==null) {
            JOptionPane.showMessageDialog(rootPane, "Debe ingresar la fecha de actividad");
//            ActualizarMant.datefechamant.setDate(null);
            ActualizarMant.datefechamant.grabFocus();
            return;
        } 
                   
            Mantenimiento_ mt=new Mantenimiento_();

           mt.setIDMANTENIMIENTO(ActualizarMant.txtcodigo.getText());
           mt.setNOMBRE(ActualizarMant.txtnombre.getText());
           mt.setIMPORTE(Double.parseDouble(ActualizarMant.txtimporte.getText()));
            if(ActualizarMant.datefechamant.getDate()==null){
              ActualizarMant.txt1.setText("");
           }
           mt.setFECHAMANT(ActualizarMant.txt1.getText());
           
           mt.setIdproveedor(ActualizarMant.txtidp.getText());
           mt.setRazonsocial(ActualizarMant.txtnomp.getText());
           
          String valor=ActualizarMant.cbtm.getSelectedItem().toString();
	   mt.setIdtipom(dao.verificarTipoMantenimiento(valor));
           
           if(ActualizarMant.dateultimarevision.getDate()==null){
              ActualizarMant.txt2.setText("");
           }
           mt.setULTIMAREV(ActualizarMant.txt2.getText());
           mt.setDIASVEN(Integer.parseInt(ActualizarMant.txtdiasv.getText()));
           
            if(ActualizarMant.datesiguienterevision.getDate()==null){
              ActualizarMant.txt3.setText("");
           }
           mt.setSIGUIENTEREV(ActualizarMant.txt3.getText());
           mt.setULTIMAREVKM(Double.parseDouble(ActualizarMant.txturk.getText()));
           mt.setVENCIMIENTOKM(Double.parseDouble(ActualizarMant.txtvk.getText()));
           mt.setSIGTREVKM(Double.parseDouble(ActualizarMant.txtsrk.getText()));
           
           mt.setIdvehiculo(ActualizarMant.txtidv.getText());
           mt.setMaquina(ActualizarMant.txtnomv.getText());
           
           mt.setIdempleado(ActualizarMant.txtide.getText());
           mt.setNomempleado(ActualizarMant.txtnome.getText());

           
           mt.setFECHAM(ActualizarMant.datefa.getText());
           mt.setNOMUSUM(ActualizarMant.txtuser.getText());
           mt.setIP(ActualizarMant.txtip.getText());
           
          
            if(actualizar){
                resultado=dao.ModificarMantenimiento(mt);
                JOptionPane.showMessageDialog(rootPane, resultado);
                enableFalse();
                
                dao.mostrarManteniento("", "", "", "", "");
                tablemantenimiento.setRowSelectionInterval(0, 0);
                quitarcolor();
            }
           dispose();

        }

    }//GEN-LAST:event_jButton2ActionPerformed

