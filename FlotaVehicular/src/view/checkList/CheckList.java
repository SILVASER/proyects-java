    private void bntcompletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntcompletarActionPerformed
        Inspeccion ins=new Inspeccion();  
        nuevo = true;
        
        ins.setCODIGO(txtcodigo.getText());
        ins.setACTIVIDAD(txtactividad.getText());
        ins.setFECHA(Utilidades.formatDate(dhfecha.getDate()));
        ins.setHORA(txthora.getText());
        ins.setUNIDAD(txtunidad.getText());
        ins.setMARCA(txtmarca.getText());
        ins.setTURNO(txtturno.getText());
        ins.setCONDUCTOR(txtconductor.getText());
        ins.setTECNICO(txttecnico.getText());
        ins.setFECHAACT(Utilidades.formatDate(fechaActual));
        ins.setUSUARIO(nombreUser);
        ins.setIP(ip);
        ins.setOBSERVACION(txtobservacion.getText());

        if(nuevo){
           String resultado=dao.GuardarInspeccion(ins);
           JOptionPane.showMessageDialog(rootPane, resultado);
        }        
       or.setCODIGO(txtcodigo.getText());
      
      
       if(nuevo){
            dao.GuardarOrden(or);
        }
                
        bntcompletar.setEnabled(false);
        
        txtcodigo.setEditable(false);
        txtactividad.setEditable(false);
        dhfecha.setEnabled(false);
        txtunidad.setEditable(false);
        txthora.setEditable(false);
        txtmarca.setEditable(false);
        txtconductor.setEditable(false);
        txttecnico.setEditable(false);
        txtturno.setEditable(false);
        txtobservacion.setEditable(false);
        
        txtcodigo.setBackground(new Color(255,255,255)); 
        txtactividad.setBackground(new Color(255,255,255)); 
        dhfecha.setBackground(new Color(255,255,255));  
        txtunidad.setBackground(new Color(255,255,255)); 
        txthora.setBackground(new Color(255,255,255)); 
        txtmarca.setBackground(new Color(255,255,255)); 
        txtconductor.setBackground(new Color(255,255,255)); 
        txttecnico.setBackground(new Color(255,255,255)); 
        txtturno.setBackground(new Color(255,255,255)); 
        txtobservacion.setBackground(new Color(255,255,255)); 
        
        jTabbedPane1.setEnabled(true);
        activarchecked();
        
        dao.mostrarChecklist("", "");
        
    }//GEN-LAST:event_bntcompletarActionPerformed

