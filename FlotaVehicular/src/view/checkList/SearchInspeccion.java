    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String fecha="",codigo="";

        fecha=txt.getText();
        codigo=txtcodigo.getText();
       
        dao.mostrarChecklist(codigo,fecha);

        if(InspeccionMovil.tableinspeccion.getRowCount()==0){
            JOptionPane.showMessageDialog(null, "No se encontraron resultados en la busqueda.!");
              dao.mostrarChecklist("","");

        }
        InspeccionMovil.tableinspeccion.setRowSelectionInterval(0, 0);

        txt.setText("");
      
        txtcodigo.setText("");

        datef.setDate(null);

        dispose();
    }//GEN-LAST:event_jButton3ActionPerformed

