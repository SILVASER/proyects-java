    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String categoria="";
Dao dao=new Dao();

        int verfilay = InspeccionMovil.tableinspeccion.getSelectedRow();
        String idy = (String) InspeccionMovil.tableinspeccion.getValueAt(verfilay, 0);
        
        categoria=txtcategoria.getText();

        dao.MOSTRARORDEN(idy,categoria);

        if(InspeccionMovil.tableorden.getRowCount()==0){
            JOptionPane.showMessageDialog(null, "No se encontraron resultados en la busqueda.!");
            dao.MOSTRARORDEN(idy,"");

        }
       InspeccionMovil.tableorden.setRowSelectionInterval(0, 0);

        txtcategoria.setText("");
      

        dispose();
    }//GEN-LAST:event_jButton3ActionPerformed

