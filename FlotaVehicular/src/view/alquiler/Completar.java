    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (cbact.getSelectedItem().equals("Actualizar")) {
            enableTrue();
            dispose();
            darColor();
            ActualizarVehiculo.txtmatricula.grabFocus();
        }

        if (cbact.getSelectedItem().equals("Guardar")) {
            actualizar = true;
            VehiculosAquiler vhqw = new VehiculosAquiler();

            vhqw.setIDVEHICULO(ActualizarVehiculo.txtcodigo.getText());

            if (ActualizarVehiculo.datefr.getDate() == null) {
                ActualizarVehiculo.txt.setText("");
            }

            vhqw.setFECHACOMPRA(ActualizarVehiculo.txt.getText());
            vhqw.setMATRICULA(ActualizarVehiculo.txtmatricula.getText());
            vhqw.setPLACA(ActualizarVehiculo.txtplaca.getText());
            vhqw.setMARCA(ActualizarVehiculo.txtmarca.getText());
            vhqw.setMODELO(ActualizarVehiculo.txtmodelo.getText());
            vhqw.setPOTENCIA(ActualizarVehiculo.txtpotencia.getText());
            vhqw.setSERIEMOTOR(ActualizarVehiculo.txtsm.getText());

            valor = ActualizarVehiculo.cbtv.getSelectedItem().toString();
            vhqw.setTIPOVEHICULO(valor);

            vhqw.setSTOCK(Integer.parseInt(ActualizarVehiculo.txtstock1.getText()));
            vhqw.setCOSTOALQUILER(Double.parseDouble(ActualizarVehiculo.txtcosto.getText()));

            vhqw.setDESCRIPCION(ActualizarVehiculo.txtdescripcion.getText());

            String ope;
            if (ActualizarVehiculo.ckdisponible.isSelected()) {
                ope = "DISPONIBLE";
            } else {
                ope = "NO DISPONIBLE";
            }

            vhqw.setESTADO(ope);

            vhqw.setFECHAM(ActualizarVehiculo.txtfmodif.getText());
            vhqw.setNOMUSUM(ActualizarVehiculo.txtuser1.getText());
            vhqw.setIP(ActualizarVehiculo.txtip.getText());

            if (actualizar) {
                resultado = dao.ModificarVehiculoAlquiler(vhqw);
                JOptionPane.showMessageDialog(rootPane, resultado);
                dao.mostrarVehiculosAlquiler("", "", "", "", "", "");
                dao.mostrarVehiculosDisponibles("", "", "", "", "", "");
                dao.mostrarVehiculosNoDisponibles("", "", "", "", "", "");

                if (RegistVehiculosAlq.tablevehiculos.getRowCount() != 0) {
                    RegistVehiculosAlq.tablevehiculos.setRowSelectionInterval(0, 0);
                }

                if (RegistVehiculosAlq.tabledisponibles.getRowCount() != 0) {
                    RegistVehiculosAlq.tabledisponibles.setRowSelectionInterval(0, 0);
                }

                if (RegistVehiculosAlq.tablenodisponibles.getRowCount() != 0) {
                    RegistVehiculosAlq.tablenodisponibles.setRowSelectionInterval(0, 0);
                }
                enableFalse();
                quitarColor();
            }
            dispose();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

