    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (cbact.getSelectedItem().equals("Actualizar")) {
            enableTrue();
            dispose();
            darColor();
            ActualizarAlquiler.txtnombre.grabFocus();
        }

        if (cbact.getSelectedItem().equals("Guardar")) {
            actualizar = true;

            Alquiler a = new Alquiler();

            a.setIDALQUILER(ActualizarAlquiler.txtcodigo.getText());
            a.setIDCLIENTE(ActualizarAlquiler.txtidcliente.getText());
            a.setNOMCLIENTE(ActualizarAlquiler.txtnombre.getText());
            a.setNDOC(ActualizarAlquiler.txtndoc.getText());
            a.setOBRACLIENTE(ActualizarAlquiler.txtobra.getText());
            a.setNUMOBRA(Integer.parseInt(ActualizarAlquiler.txtnobra.getText()));
            a.setTELCLIENTE(ActualizarAlquiler.txttelefono.getText());
            a.setIDVEHALQUILER(Integer.parseInt(ActualizarAlquiler.txtidvehiculo.getText()));
            a.setTIPOVEHICULO(ActualizarAlquiler.txttipoveh.getText());
            a.setMATRICULA(ActualizarAlquiler.txtmatricula.getText());
            a.setMARCA(ActualizarAlquiler.txtmarca.getText());
            a.setPLACA(ActualizarAlquiler.txtplaca.getText());
            a.setPOTENCIA(ActualizarAlquiler.txtpotencia.getText());
            a.setCOSTOVEH(Double.parseDouble(ActualizarAlquiler.txtcosto.getText()));

            a.setFECHAALQUILER(ActualizarAlquiler.txtfechaA.getText());
            a.setFECHADEVOLUCION(ActualizarAlquiler.txtfechaD.getText());
            a.setHORA(ActualizarAlquiler.txthora.getText());
            a.setUNIDADES(Integer.parseInt(ActualizarAlquiler.txtunidades.getText()));
            a.setPRECIO(Double.parseDouble(ActualizarAlquiler.txtprecio.getText()));
            a.setTOTAL(Double.parseDouble(ActualizarAlquiler.txttotal.getText()));
            a.setDIAS(Integer.parseInt(ActualizarAlquiler.txtdiasres.getText()));
            a.setTOTALDIAS(Double.parseDouble(ActualizarAlquiler.txttotaldias.getText()));
            a.setOBSERVACIONES(ActualizarAlquiler.txtobservacion.getText());
            a.setFECHAM(ActualizarAlquiler.txtfechact.getText());
            a.setNOMUSUM(ActualizarAlquiler.txtuser.getText());
            a.setIP(ActualizarAlquiler.txtip.getText());

            if (actualizar) {
                resultado = dao.modificarAlquiler(a);
                JOptionPane.showMessageDialog(rootPane, resultado);
                dao.mostrarAlquiler("", "", "", "", "", "", "", "");
                RegistAlquiler.tableAlquiler.setRowSelectionInterval(0, 0);
                enableFalse();
                quitarColor();

            }
            dispose();

        }

    }//GEN-LAST:event_jButton1ActionPerformed

