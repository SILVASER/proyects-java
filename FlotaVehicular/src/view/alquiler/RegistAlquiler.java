    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int fila = tableAlquiler.getSelectedRow();
        String activo = "";
        if (fila < 0) {
            JOptionPane.showMessageDialog(rootPane, "no ha seleccionado ningun registro");

        } else {

            Date fecha1 = null, fecha2 = null;
            // int fila = tableUsuarios.getSelectedRow();
            ActualizarAlquiler aa = new ActualizarAlquiler();
            aa.setVisible(true);
            aa.setLocationRelativeTo(null);

            ActualizarAlquiler.txtcodigo.setText(tableAlquiler.getValueAt(fila, 0).toString());
            ActualizarAlquiler.txtidcliente.setText(tableAlquiler.getValueAt(fila, 1).toString());
            ActualizarAlquiler.txtndoc.setText(tableAlquiler.getValueAt(fila, 3).toString());

            ActualizarAlquiler.txtnombre.setText(tableAlquiler.getValueAt(fila, 2).toString());
            ActualizarAlquiler.txttelefono.setText(tableAlquiler.getValueAt(fila, 4).toString());

            ActualizarAlquiler.txtobra.setText(tableAlquiler.getValueAt(fila, 5).toString());
            ActualizarAlquiler.txtnobra.setText(tableAlquiler.getValueAt(fila, 6).toString());
            ActualizarAlquiler.txtidvehiculo.setText(tableAlquiler.getValueAt(fila, 7).toString());
            ActualizarAlquiler.txttipoveh.setText(tableAlquiler.getValueAt(fila, 8).toString());
            ActualizarAlquiler.txtmatricula.setText(tableAlquiler.getValueAt(fila, 9).toString());
            ActualizarAlquiler.txtplaca.setText(tableAlquiler.getValueAt(fila, 10).toString());
            ActualizarAlquiler.txtmarca.setText(tableAlquiler.getValueAt(fila, 11).toString());
            ActualizarAlquiler.txtpotencia.setText(tableAlquiler.getValueAt(fila, 12).toString());
            ActualizarAlquiler.txtcosto.setText(tableAlquiler.getValueAt(fila, 13).toString());

            try {
                fecha1 = new SimpleDateFormat("yyyy-MM-dd").parse((String) tableAlquiler.getValueAt(fila, 14).toString());
            } catch (ParseException ex) {
                Logger.getLogger(RegistAlquiler.class.getName()).log(Level.SEVERE, null, ex);
            }

            ActualizarAlquiler.dateAlq.setDate(fecha1);

            ActualizarAlquiler.txthora.setText(tableAlquiler.getValueAt(fila, 15).toString());

            try {
                fecha2 = new SimpleDateFormat("yyyy-MM-dd").parse((String) tableAlquiler.getValueAt(fila, 16).toString());

            } catch (ParseException ex) {
                Logger.getLogger(RegistAlquiler.class.getName()).log(Level.SEVERE, null, ex);
            }

            ActualizarAlquiler.datefr.setDate(fecha2);

            ActualizarAlquiler.txtunidades.setText(tableAlquiler.getValueAt(fila, 17).toString());
            ActualizarAlquiler.txtprecio.setText(tableAlquiler.getValueAt(fila, 18).toString());
            ActualizarAlquiler.txttotal.setText(tableAlquiler.getValueAt(fila, 19).toString());
            ActualizarAlquiler.txtdiasres.setText(tableAlquiler.getValueAt(fila, 20).toString());
            ActualizarAlquiler.txttotaldias.setText(tableAlquiler.getValueAt(fila, 21).toString());

            ActualizarAlquiler.txtobservacion.setText(tableAlquiler.getValueAt(fila, 22).toString());
            ActualizarAlquiler.txtfechact.setText(tableAlquiler.getValueAt(fila, 23).toString());
            ActualizarAlquiler.txtuser.setText(tableAlquiler.getValueAt(fila, 24).toString());
            ActualizarAlquiler.txtip.setText(tableAlquiler.getValueAt(fila, 25).toString());

            ActualizarAlquiler.jButton10.grabFocus();

        }

    }//GEN-LAST:event_jButton3ActionPerformed

