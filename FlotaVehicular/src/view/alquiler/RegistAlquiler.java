    public void ptable() {

        tableAlquiler.getTableHeader().setDefaultRenderer(new DefaultTableCellRenderer() {

            @Override
            public Component getTableCellRendererComponent(JTable table, Object value,
                    boolean isSelected, boolean hasFocus, int row, int column) {

                JLabel l = (JLabel) super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);

                l.setBorder(new LineBorder(new Color(239, 242, 210), 1));

//            l.setBackground(Color.green);
//                 l.setBackground(Color.getHSBColor(800, 40, 31)); 
                l.setBackground(new Color(184, 221, 182));

                l.setHorizontalAlignment(SwingConstants.CENTER);

                return l;
            }
        });

    }

    public void up() {
        int index = 0;
        index = tableAlquiler.getSelectedRow();
        //System.out.println("up= " + index);

        if (index > 0) {
            tableAlquiler.setRowSelectionInterval(index - 1, index - 1);
            btndouwn.setEnabled(true);
            cantidadRegist(index);

        } else {
            btnup.setEnabled(false);
            btndouwn.setEnabled(true);
        }

    }

    public void down() {
        int index;

        index = tableAlquiler.getSelectedRow();

        if (tableAlquiler.getRowCount() > (index + 1)) {
            tableAlquiler.setRowSelectionInterval(index + 1, index + 1);

            cantidadRegist(index + 2);
            btnup.setEnabled(true);
        } else {
            btndouwn.setEnabled(false);
            btnup.setEnabled(true);

        }
    }

    public void cantidadRegist(int j) {
        int fila = tableAlquiler.getSelectedRow();
        int x = 0;
        for (int i = 0; i < tableAlquiler.getRowCount(); i++) {
            x++;
        }

        cantidad.setText("[" + j + "/" + String.valueOf(x) + "]");
    }

