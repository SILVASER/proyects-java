    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            String valor="";
        if(cbact.getSelectedItem().equals("Actualizar")) {
                enableTrue();
		dispose();
                darColor();
                ActualizarCliente.txtndoc.grabFocus();
                
					}

						
	if(cbact.getSelectedItem().equals("Guardar")) {
            actualizar=true;
            
        if (ActualizarCliente.txtndoc.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Debe digitar N째 de documento");
           ActualizarCliente.txtndoc.grabFocus();
            return;
        }
        
         if (ActualizarCliente.txtnombre.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Debe digitar un nombre");
           ActualizarCliente.txtndoc.grabFocus();
            return;
        }
         
          if (ActualizarCliente.txtdireccion.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Debe digitar una direcci처n");
           ActualizarCliente.txtdireccion.grabFocus();
            return;
        }
          
           if (!Utilidades.isNumeric(ActualizarCliente.txtnobra.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Debe digitar un valor numerico");
           ActualizarCliente.txtnobra.grabFocus();
            return;
        }
        
        
         Cliente cl=new Cliente();
         cl.setCodigo(ActualizarCliente.txtid.getText());
         
        valor=ActualizarCliente.cbtipodoc.getSelectedItem().toString();
	cl.setTipodoc(valor);	
        
        cl.setNdoc(ActualizarCliente.txtndoc.getText());
        cl.setNombre(ActualizarCliente.txtnombre.getText());
        cl.setTelefono(ActualizarCliente.txttelefono.getText());
        cl.setContacto(ActualizarCliente.txtcontacto.getText());
        cl.setCodigopostal(ActualizarCliente.txtcpostal.getText());
        cl.setDireccion(ActualizarCliente.txtdireccion.getText());
        cl.setPais(ActualizarCliente.txtpais.getText());
        cl.setDepartamento(ActualizarCliente.txtdep.getText());
        cl.setProvincia(ActualizarCliente.txtprov.getText());
        cl.setDistrito(ActualizarCliente.txtdis.getText());
        
        
        cl.setObra(ActualizarCliente.txtobra.getText());
        cl.setNunobra(Integer.parseInt(ActualizarCliente.txtnobra.getText()));
        cl.setObservaciones(ActualizarCliente.txtobservaciones.getText());
        cl.setNomusum(ActualizarCliente.txtuser1.getText());
        cl.setFecham(ActualizarCliente.txtfmodif.getText());
        cl.setIp(ActualizarCliente.txtip.getText());
        cl.setEmail(ActualizarCliente.txtemail.getText());
 
        

        String activo;
        if(ActualizarCliente.chactivo.isSelected())
        {
            activo = "1";
        }else
        {
            activo = "0";
        }
        cl.setActivo(activo);

							if(actualizar){
								resultado=dao.modificarCliente(cl);
								JOptionPane.showMessageDialog(rootPane, resultado);
								dao.mostrarClientes("","","","","");
                                                                RegistClientes.tableClientes.setRowSelectionInterval(0, 0);
                                                                enableFalse();
                                                                quitarColor();

							}
							dispose();
							
                                                }
    
    }//GEN-LAST:event_jButton1ActionPerformed

