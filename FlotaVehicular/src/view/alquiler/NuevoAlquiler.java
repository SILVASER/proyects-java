    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        nuevo = true;

        if (txtunidades.getText().equals("0")) {
            JOptionPane.showMessageDialog(null, "Debe ingresar las unidades de veh√≠culos alquilados");
            return;
        }
        if (txtcosto.getText().equals("0.00")) {
            JOptionPane.showMessageDialog(null, "Debe ingresar el costo del veh√≠culo");
            return;
        }

        Alquiler a = new Alquiler();
        a.setIDALQUILER(txtcodigo.getText());
        a.setIDCLIENTE(txtidcliente.getText());
        a.setNOMCLIENTE(txtnombre.getText());
        a.setNDOC(txtndoc.getText());
        a.setOBRACLIENTE(txtobra.getText());
        a.setNUMOBRA(Integer.parseInt(txtnobra.getText()));
        a.setTELCLIENTE(txttelefono.getText());
        a.setIDVEHALQUILER(Integer.parseInt(txtidvehiculo.getText()));
        a.setTIPOVEHICULO(txttipoveh.getText());
        a.setMATRICULA(txtmatricula.getText());
        a.setMARCA(txtmarca.getText());
        a.setPLACA(txtplaca.getText());
        a.setPOTENCIA(txtpotencia.getText());
        a.setCOSTOVEH(Double.parseDouble(txtcosto.getText()));

        a.setFECHAALQUILER(txtfechaA.getText());
        a.setFECHADEVOLUCION(txtfechaD.getText());
        a.setHORA(txthora.getText());
        a.setUNIDADES(Integer.parseInt(txtunidades.getText()));
        a.setPRECIO(Double.parseDouble(txtprecio.getText()));
        a.setTOTAL(Double.parseDouble(txttotal.getText()));
        a.setDIAS(Integer.parseInt(txtdiasres.getText()));
        a.setTOTALDIAS(Double.parseDouble(txttotaldias.getText()));

        a.setOBSERVACIONES(txtobservacion.getText());
        a.setFECHAM(txtfechact.getText());
        a.setNOMUSUM(txtuser.getText());
        a.setIP(txtip.getText());

        if (nuevo) {
            resultado=dao.GuardarAlquiler(a);
            JOptionPane.showMessageDialog(rootPane, resultado);
            dao.mostrarAlquiler("", "", "", "", "", "", "", "");
            RegistAlquiler.tableAlquiler.setRowSelectionInterval(0, 0);

        }
        dispose();

    }//GEN-LAST:event_jButton9ActionPerformed

