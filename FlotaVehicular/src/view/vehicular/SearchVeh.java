    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
     String matricula="",tipov="",marca="",modelo="",numeroserie="",fecha="",placa="";
        
        fecha=txt.getText();
        tipov=cbtipov.getSelectedItem().toString();           
        marca=txtmarca.getText();
        modelo=txtmodelo.getText();
        numeroserie=txtnumeros.getText();
        matricula=txtmatricula.getText();
        placa=txtplaca.getText();

        dao.mostrarVehiculos(matricula,tipov,placa,marca,modelo,numeroserie,fecha);
        
        if(RegisVehiculos.tablevehiculos.getRowCount()==0){
           JOptionPane.showMessageDialog(null, "No se encontraron resultados en la busqueda.!");
            dao.mostrarVehiculos("","","","","","","");
        }
        RegisVehiculos.tablevehiculos.setRowSelectionInterval(0, 0);

        txt.setText("");
        txtmarca.setText("");
        txtmodelo.setText("");
        txtnumeros.setText("");
        txtmatricula.setText("");
       
        datef.setDate(null);

        dispose();
    }//GEN-LAST:event_jButton3ActionPerformed

