    private void jdinicioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jdinicioKeyTyped
         char cTeclaPresionada=evt.getKeyChar();
        if(cTeclaPresionada==KeyEvent.VK_ENTER){
               jdfinal.grabFocus();
            }
        
         cTeclaPresionada=evt.getKeyChar();
        if(cTeclaPresionada==KeyEvent.VK_ESCAPE){
                 dispose();
            }
    }//GEN-LAST:event_jdinicioKeyTyped

