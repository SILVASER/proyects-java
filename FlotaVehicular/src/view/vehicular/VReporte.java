 public void reportevehiculo(){
           conectar c = new conectar();
    
     Date fechaActual =new Date();
        try {
            String master = System.getProperty("user.dir") + "/src/Reportes/_rVehiculo.jasper";

            Map parametro = new HashMap();

            parametro.put("fecha1", Utilidades.formatDate(desde.getDate()));
//                System.out.println(Utilidades.formatDate3(desde.getDate()));
            parametro.put("fecha2", Utilidades.formatDate(hasta.getDate()));
            parametro.put("fechaActual", Utilidades.formatDate(fechaActual));
//             System.out.println(Utilidades.formatDate3(hasta.getDate()));
            parametro.put("nmatric", txtmatric.getText());
//            parametro.put("fecha1", "05/05/2019");
//            parametro.put("fecha2", "05/05/2019");
//            parametro.put("nmatric", txtmatric.getText());

            JasperPrint informe = JasperFillManager.fillReport(master, parametro, c.getConnection());

            JasperViewer.viewReport(informe, false);
            this.dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,"Error en lanzar reporte" + ex.getMessage().toString());
        }
    }
