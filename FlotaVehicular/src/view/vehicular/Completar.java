    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            
        if(cbact.getSelectedItem().equals("Actualizar")) {
                enableTrue();
                darcolor();
		dispose();
               Actualizar.txtmatricula.grabFocus();
					}

						
						if(cbact.getSelectedItem().equals("Guardar")) {
							actualizar=true;
                                                         if ( Actualizar.txtmatricula.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Debe digitar la matricula del vehiculo");
           Actualizar.txtmatricula.grabFocus();
            return;
        }
       
        if ( Actualizar.datefr.getDate()== null) {
            JOptionPane.showMessageDialog(rootPane, "Debe digitar la fecha de ingreso");
            Actualizar.datefr.grabFocus();
            return;
        }
                                                            Vehiculo vh=new Vehiculo();

//							
                                                        
							vh.setIdvehiculo(Actualizar.txtcodigo.getText());
							vh.setMatricula(Actualizar.txtmatricula.getText());
//                                                        System.out.println(Actualizar.txtmatricula.getText());

							vh.setMaquina(Actualizar.txtmaquina.getText());
							// String userr="x3";
							// u.setUsername(userr);
							vh.setPlaca(Actualizar.txtplaca.getText());


							valor=Actualizar.cbtv.getSelectedItem().toString();
							vh.setIdTipo(dao.verificarTipoVH(valor));
                                                        
//                                                        System.out.println("sss= " + dao.verificarTipoVH(valor));
                                                        
                                                       
                                                     
                                                        vh.setMarca(Actualizar.txtmarca.getText());

                                                     
                                                        vh.setModelo(Actualizar.txtmodelo.getText());

                                                        
                                                        vh.setNumserie(Actualizar.txtserie.getText());
                                                        vh.setSeriemotor(Actualizar.txtsm.getText());
                                                        vh.setNumfactura(Actualizar.txtnf.getText());
                                                        vh.setNumneumaticos(Integer.parseInt(Actualizar.txtnn.getText()));
                                                        vh.setResponsable(Actualizar.txtres.getText());

                                                        vh.setFecregistro(Actualizar.txt.getText());

                                                        
                                                       
                                                                 
                                                        String ope;
							if (Actualizar.ckoperario.isSelected()) {
								ope = "1";
							} else {
								ope = "0";
							} 
                                                        
                                                        vh.setOperativo(ope);
                                                        
                                                       String funcion=Actualizar.cbfuncion.getSelectedItem().toString();
							vh.setFuncion(funcion);
                                                        
                                                          vh.setFecham(Actualizar.txtfmodif.getText());
                                                             vh.setNumusum(Actualizar.txtuser1.getText());
                                                            vh.setIp(Actualizar.txtip.getText());



                                    
							
							
							if(actualizar){
								resultado=dao.ModificarVehiculo(vh);
								JOptionPane.showMessageDialog(rootPane, resultado);
								dao.mostrarVehiculos("","","","","","","");
                                                                    tablevehiculos.setRowSelectionInterval(0, 0);
                                                                enableFalse();
                                                                quitarcolor();

							}
							//dispose();
							
                                                }
    
    }//GEN-LAST:event_jButton1ActionPerformed

